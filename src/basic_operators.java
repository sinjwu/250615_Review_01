public class basic_operators {
    public static void main(String[] args) {
        int a = 15; int b = 28;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(--b);
        System.out.println(a);
        System.out.println(b);
        System.out.println(a += 3);
        System.out.println(a -= 13);
    }
}
